// Каждый член экипажа добавляет к осадке корабля 1,5 единицы. Если после снятия веса экипажа осадка все равно больше 20, то корабль стоит разграбить. На корабле такого веса должно быть много добычи!Добавьте метод стоит того чтобы решить, достоин ли корабль разграбления. Например:titanic.isWorthIt() // return false

class Ship {
  constructor(draft, crew) {
    this.draft = draft;
    this.crew = crew;
  }

  isWorthIt() {
    // Вычисляем вес корабля с учетом веса экипажа
    const totalWeight = this.draft - this.crew * 1.5;

    // Проверяем, стоит ли корабль разграбления
    return totalWeight > 20;
  }
}

// Пример использования
